class Switch
{
public static void main(String args[])
{


int i=56;
		switch(i)
		{
		case 1 : System.out.println("this is a first statement");
		break;
		case 2 : System.out.println("this is a second statement");
		break;
		case 5 : System.out.println("this is a third statement");
		break;
		
		default : System.out.println("this is a default statement");
		break;
		}
}
}